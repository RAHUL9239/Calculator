package com;
public class Calculatorlmpl implements Calculator
{

	@Override
	public int add(int a, int b) {
		return a+b;
	}

	@Override
	public int sub(int a, int b) {
		return a-b;
	}

	@Override
	public int mul(int a, int b) {
		return a*b;
	}

	@Override
	public int dic(int a, int b) {
		return a/b;
	}

}

